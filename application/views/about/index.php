                    <h3 class="font-weight-bold about-text">Tentang Kantah BPN Kota Bogor</h3>
                        <p>Kantor Pertanahan ATR/BPN Kota Bogor merupakan Kantor Pelayanan dalam bidang Agraria, merupakan perpanjangan pelayanan bagi masyarakat setempat dari seluruh program yang dibuat oleh Kementerian Agraria dan Tata Ruang/Badan Pertahanan Nasional.

                        Area wilayah kerja yang menjadi tanggungjawab Kantor Pertanahan Kota Bogor untuk mengolah, mengatur, memelihara data, membuat solusi, menangani seluruh persoalan pertanahan yang berada dalam wilayah Kota Bogor bukanlah suatu pekerjaan yang mudah karena begitu kompleks nya berbagai permasalahan yang ada baik antara instansi dengan instansi, badan hukum dengan badan hukum, instansi dengan badan hukum, masyarakat dengan masyarakat, masyarakat dengan instansi, masyarakat dengan badan hukum dan sebagai.

